#include<iostream>
using namespace std;
int main(){
    // Arithmatic operator
    int a=2.0/5;
    cout<<a<<endl;

    cout<<2.0/5<<endl;

    // Relational operator
    int x=2;
    int y=3;

    bool first=(x==y);
    cout<<first<<endl;

    bool second=(x>y);
    cout<<second<<endl;

    bool third=(x<y);
    cout<<third<<endl;

    bool fourth=(x>=y);
    cout<<fourth<<endl;

    bool fifth=(x<=y);
    cout<<fifth<<endl;

    bool six=(x!=y);
    cout<<six<<endl;

}